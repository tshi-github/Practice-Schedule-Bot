async function handleHelpCommand(message) {
  const help = [
    '📋 **コマンド一覧**',
    '',
    '**!event** - 練習イベントを登録してリアクション投票を作成',
    '```',
    '!event 月/日 開始時刻-終了時刻 イベント名',
    '例: !event 10/1 19:00-21:00 メガロバニア',
    '```',
    '複数行まとめて送信も可能：',
    '```',
    '!event',
    '10/1 19:00-21:00 メガロバニア',
    '10/2 17:00-21:00 自由曲',
    '```',
    '',
    '**!booking** - F1会議室の空き状況を確認',
    '```',
    '!booking 2026/4/1 10:00-12:00',
    '```',
    '複数行まとめて送信も可能：',
    '```',
    '!booking',
    '2026/4/1 10:00-12:00',
    '2026/4/2 13:00-15:00',
    '```',
    '',
    '**!help** - このメッセージを表示',
  ].join('\n');

  await message.channel.send(help);
}

module.exports = { handleHelpCommand };